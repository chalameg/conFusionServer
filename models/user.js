var mongoose = require('mongoose');
var Schema = mongoose.Schema;
//this is mongoose plugin wich add username and password fields and hash our
//password using salt before it is saved to database
var passportLocalMongoose = require('passport-local-mongoose');

var User = new Schema({
    firstname: {
        type: String,
        default: ''
    },
    lastname: {
        type: String,
        default: ''
    },
    facebookId: String,
    admin:   {
        type: Boolean,
        default: false
    }
});

User.plugin(passportLocalMongoose);

module.exports = mongoose.model('User', User);